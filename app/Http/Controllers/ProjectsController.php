    {
        $consultants = DB::table('users')
            ->join('role_user', 'role_user.user_id', '=', 'users.id')
            ->where('role_id', '=', 2)
            ->get();
        $templates = Template::all();
        return view('projects.create')
            ->with('templates',$templates)
            ->with('consultants',$consultants);
